export function getColors<T extends string>(
  colors: Record<T, string | string[]>,
  mode: "light" | "dark",
): Record<T, string> {
  return Object.entries(colors).reduce<Record<string, string>>(
    (acc, [key, color]) => {
      acc[key] = Array.isArray(color) ? color[mode === "light" ? 0 : 1] : color;
      return acc;
    },
    {},
  );
}

